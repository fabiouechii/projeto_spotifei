    public void atualizarTabelaMusicas(List<Musica> musicas, String termoOriginal) {
        String[] colunas = {"ID", "Nome", "Autor", "Gênero"};
        DefaultTableModel model = new DefaultTableModel(colunas, 0) {
            @Override
            public boolean isCellEditable(int row, int column) {
                return false;
            }
        };
    
        if (musicas != null) {
            for (Musica musica : musicas) {
                model.addRow(new Object[]{
                    musica.getIdMusica(),
                    musica.getNomeMusica(),
                    musica.getAutorMusica(),
                    musica.getGeneroMusica()
                });
            }
        }
        
        table_resultadoPesquisa.setModel(model);
        
        if (musicas != null && musicas.isEmpty() && termoOriginal != null && !termoOriginal.trim().isEmpty()) {
            JOptionPane.showMessageDialog(this, "Nenhuma música encontrada para '" + termoOriginal + "'.", "Sem resultados", JOptionPane.INFORMATION_MESSAGE);
        }
    }
    
    public void popularTabelaMusicas(List<Musica> musicas, String termoOriginal) {
    String[] colunas = {"ID", "Nome", "Autor", "Gênero"};
    DefaultTableModel model = new DefaultTableModel(colunas, 0) {
        @Override
        public boolean isCellEditable(int row, int column) {
            return false;
        }
    };

    if (musicas != null) {
        for (Musica musica : musicas) {
            model.addRow(new Object[]{
                musica.getIdMusica(),
                musica.getNomeMusica(),
                musica.getAutorMusica(),
                musica.getGeneroMusica()
            });
        }
    }

    table_resultadoPesquisa.setModel(model);

    if (musicas != null && musicas.isEmpty() && termoOriginal != null && !termoOriginal.trim().isEmpty()) {
        JOptionPane.showMessageDialog(this, "Nenhuma música encontrada para '" + termoOriginal + "'.", "Sem resultados", JOptionPane.INFORMATION_MESSAGE);
    }
}
    
    public void limparTabela() {
    DefaultTableModel model = (DefaultTableModel) table_resultadoPesquisa.getModel();
    model.setRowCount(0);
}
        
